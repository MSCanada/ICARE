<?php
class options{
	public function options($OptionID,$OptionOrderNumber,$OptionTextEN,$OptionTextFR,$QuestionNumber,$NextQuestionNumber,$OptionType,$RiskType){
		$this->OptionID=$OptionID;
		$this->OptionOrderNumber=$OptionOrderNumber;
		$this->OptionTextEN=$OptionTextEN;
		$this->OptionTextFR=$OptionTextFR;
		$this->QuestionNumber=$QuestionNumber;
		$this->NextQuestionNumber=$NextQuestionNumber;
		$this->OptionType=$OptionType;
		$this->RiskType=$RiskType;
		
		
	}
	
	
}