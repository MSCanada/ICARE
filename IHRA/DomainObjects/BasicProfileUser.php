<?php
class BasicProfileUser{
	public function BasicProfileUser($UserID,$FirstName,$LastName,$EmailAddress,$Password,$Age,$Gender,$EmployerGroup,$Department,$ProvinceID,$Language,$UserStatus,$IsPrimaryPolicyHolder,$CertNumber,$CreatedDate){
	$this->UserID=$UserID;
	$this->FirstName=$FirstName;
	$this->LastName=$LastName;
	$this->EmailAddress=$EmailAddress;
	$this->Password=$Password;
	$this->Age=$Age;
	$this->Gender=$Gender;
	$this->EmployerGroup=$EmployerGroup;
	$this->Department=$Department;
	$this->ProvinceID=$ProvinceID;
	$this->Language=$Language;
	$this->UserStatus=$UserStatus;
	$this->IsPrimaryPolicyHolder=$IsPrimaryPolicyHolder;
	$this->CertNumber=$CertNumber;
	$this->CreatedDate=$CreatedDate;	
	}
	
	
	
	
	
	
}